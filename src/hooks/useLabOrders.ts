// Update lab order item with results
export function useUpdateLabOrderItem() {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: async ({
      id,
      result_values,
      result_notes,
      status,
      performed_by,
    }: {
      id: string;
      result_values?: Record<string, string | number>;
      result_notes?: string;
      status?: "pending" | "collected" | "processing" | "completed";
      performed_by?: string;
    }) => {
      const updateData: Record<string, unknown> = {};
      
      if (result_values !== undefined) updateData.result_values = result_values;
      if (result_notes !== undefined) updateData.result_notes = result_notes;
      if (status !== undefined) updateData.status = status;
      if (performed_by !== undefined) updateData.performed_by = performed_by;
      if (status === "completed") updateData.result_date = new Date().toISOString();

      const { data, error } = await supabase
        .from("lab_order_items")
        .update(updateData)
        .eq("id", id)
        .select()
        .single();

      if (error) throw error;
      return data;
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ["lab-orders"] });
      queryClient.invalidateQueries({ queryKey: ["lab-order"] });
    },
  });
}

// Mark sample as collected with sample number
export function useMarkSampleCollected() {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: async ({ orderId, sampleNumber }: { orderId: string; sampleNumber: string }) => {
      labLogger.info("Marking sample as collected", { orderId, sampleNumber });

      // Update lab order status and sample number
      const { error: orderError } = await supabase
        .from("lab_orders")
        .update({ 
          status: "collected" as const,
          sample_number: sampleNumber
        })
        .eq("id", orderId);

      if (orderError) {
        labLogger.error("Failed to mark sample collected", orderError, { orderId });
        throw orderError;
      }

      // Update all items to collected
      const { error: itemsError } = await supabase
        .from("lab_order_items")
        .update({ status: "collected" as const })
        .eq("lab_order_id", orderId);

      if (itemsError) {
        labLogger.error("Failed to update lab order items", itemsError, { orderId });
        throw itemsError;
      }

      labLogger.info("Sample collected", { orderId, sampleNumber });

      return true;
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ["lab-orders"] });
      queryClient.invalidateQueries({ queryKey: ["lab-order"] });
    },
  });
}

// Complete lab order (mark all tests as completed)
export function useCompleteLabOrder() {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: async ({
      orderId,
      result_notes,
    }: {
      orderId: string;
      result_notes?: string;
    }) => {
      labLogger.info("Completing lab order", { orderId });

      const { data, error } = await supabase
        .from("lab_orders")
        .update({
          status: "completed" as const,
          completed_at: new Date().toISOString(),
          result_notes,
        })
        .eq("id", orderId)
        .select()
        .single();

      if (error) {
        labLogger.error("Failed to complete lab order", error, { orderId });
        throw error;
      }

      labLogger.info("Lab order completed", { 
        orderId, 
        orderNumber: data.order_number,
        completedAt: data.completed_at
      });

      return data;
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ["lab-orders"] });
      queryClient.invalidateQueries({ queryKey: ["lab-order"] });
    },
  });
}


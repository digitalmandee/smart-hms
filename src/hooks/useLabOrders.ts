import { useQuery, useMutation, useQueryClient } from "@tanstack/react-query";
import { supabase } from "@/integrations/supabase/client";
import type { Tables, TablesInsert } from "@/integrations/supabase/types";
import { labLogger } from "@/lib/logger";

export type LabOrder = Tables<"lab_orders"> & {
  invoice_id?: string | null;
  payment_status?: "pending" | "paid" | "partial" | "waived";
  ordered_by?: string | null;
};
export type LabOrderItem = Tables<"lab_order_items">;
export type LabOrderInsert = TablesInsert<"lab_orders">;
export type LabOrderItemInsert = TablesInsert<"lab_order_items">;

export interface LabOrderItemInput {
  service_type_id?: string;
  test_name: string;
  test_category: string;
  instructions?: string;
  price?: number;
}

export interface LabOrderWithItems extends LabOrder {
  items?: LabOrderItem[];
  patient?: Tables<"patients">;
  doctor?: Tables<"doctors"> & { profile?: Tables<"profiles"> };
  invoice?: Tables<"invoices"> | null;
}

interface LabOrderFilters {
  patientId?: string;
  consultationId?: string;
  surgeryId?: string;
  status?: string;
  branchId?: string;
  paymentStatus?: string;
}

// Fetch lab orders with optional filters
export function useLabOrders(filters: LabOrderFilters = {}) {
  return useQuery({
    queryKey: ["lab-orders", filters],
    queryFn: async () => {
      let query = (supabase as any)
        .from("lab_orders")
        .select(`
          *,
          items:lab_order_items(*),
          patient:patients(id, first_name, last_name, patient_number, date_of_birth, gender, phone),
          doctor:doctors(id, profile:profiles(full_name)),
          invoice:invoices(id, invoice_number, total_amount, paid_amount, status)
        `)
        .order("created_at", { ascending: false });

      if (filters.patientId) {
        query = query.eq("patient_id", filters.patientId);
      }
      if (filters.consultationId) {
        query = query.eq("consultation_id", filters.consultationId);
      }
      if (filters.surgeryId) {
        query = query.eq("surgery_id", filters.surgeryId);
      }
      if (filters.status) {
        query = query.eq("status", filters.status as "ordered" | "collected" | "processing" | "completed" | "cancelled");
      }
      if (filters.branchId) {
        query = query.eq("branch_id", filters.branchId);
      }
      if (filters.paymentStatus) {
        query = query.eq("payment_status", filters.paymentStatus);
      }

      const { data, error } = await query;
      if (error) throw error;
      return data as LabOrderWithItems[];
    },
  });
}

// Fetch single lab order with full details
export function useLabOrder(id: string | undefined) {
  return useQuery({
    queryKey: ["lab-order", id],
    queryFn: async () => {
      if (!id) return null;
      const { data, error } = await supabase
        .from("lab_orders")
        .select(`
          *,
          items:lab_order_items(*),
          patient:patients(id, first_name, last_name, patient_number, date_of_birth, gender, phone),
          doctor:doctors(id, specialization, qualification, license_number, profile:profiles(full_name))
        `)
        .eq("id", id)
        .single();

      if (error) throw error;
      return data as LabOrderWithItems;
    },
    enabled: !!id,
  });
}

// Fetch lab order by consultation

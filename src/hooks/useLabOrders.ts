// Create lab order with items and optional invoice
export function useCreateLabOrder() {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: async ({
      labOrder,
      items,
      createInvoice = false,
      organizationId,
    }: {
      labOrder: {
        consultation_id?: string;
        patient_id: string;
        doctor_id?: string;
        branch_id: string;
        organization_id?: string;
        priority?: "routine" | "urgent" | "stat";
        clinical_notes?: string;
        ordered_by?: string;
      };
      items: LabOrderItemInput[];
      createInvoice?: boolean;
      organizationId?: string;
    }) => {
      labLogger.info("Creating lab order", { 
        consultationId: labOrder.consultation_id,
        patientId: labOrder.patient_id,
        testsCount: items.length,
        priority: labOrder.priority || "routine",
        tests: items.map(i => i.test_name),
        createInvoice
      });

      // Create lab order (order_number auto-generated by trigger)
      const { data: order, error: orderError } = await (supabase as any)
        .from("lab_orders")
        .insert({
          order_number: "TEMP",
          consultation_id: labOrder.consultation_id || null,
          patient_id: labOrder.patient_id,
          doctor_id: labOrder.doctor_id || null,
          branch_id: labOrder.branch_id,
          priority: labOrder.priority || "routine",
          clinical_notes: labOrder.clinical_notes,
          ordered_by: labOrder.ordered_by || null,
          payment_status: "pending",
        })
        .select()
        .single();

      if (orderError) {
        labLogger.error("Failed to create lab order", orderError, { consultationId: labOrder.consultation_id });
        throw orderError;
      }

      // Create lab order items
      if (items.length > 0) {
        const itemsToInsert = items.map((item) => ({
          lab_order_id: order.id,
          service_type_id: item.service_type_id || null,
          test_name: item.test_name,
          test_category: item.test_category,
          instructions: item.instructions || null,
        }));

        const { error: itemsError } = await supabase
          .from("lab_order_items")
          .insert(itemsToInsert);

        if (itemsError) {
          labLogger.error("Failed to create lab order items", itemsError, { orderId: order.id });
          throw itemsError;
        }
      }

      // Create invoice if requested
      let invoice = null;
      if (createInvoice && organizationId && items.length > 0) {
        const today = new Date();
        const dateStr = today.toISOString().slice(0, 10).replace(/-/g, "");
        const randomSuffix = Math.floor(Math.random() * 1000).toString().padStart(3, "0");
        const invoiceNumber = `INV-${dateStr}-${randomSuffix}`;

        const subtotal = items.reduce((sum, item) => sum + (item.price || 0), 0);

        const { data: invoiceData, error: invoiceError } = await supabase
          .from("invoices")
          .insert({
            invoice_number: invoiceNumber,
            patient_id: labOrder.patient_id,
            branch_id: labOrder.branch_id,
            organization_id: organizationId,
            discount_amount: 0,
            tax_amount: 0,
            total_amount: subtotal,
            balance_amount: subtotal,
            paid_amount: 0,
            status: "pending",
            notes: `Invoice for Lab Order ${order.order_number}`,
            created_by: labOrder.ordered_by,
          })
          .select()
          .single();

        if (invoiceError) {
          labLogger.error("Failed to create invoice", invoiceError, { orderId: order.id });
          throw new Error(`Failed to create invoice: ${invoiceError.message}`);
        }
        
        invoice = invoiceData;

        // Create invoice items
        const invoiceItems = items.map((item) => ({
          invoice_id: invoice.id,
          description: item.test_name,
          quantity: 1,
          unit_price: item.price || 0,
          discount_percent: 0,
          total_price: item.price || 0,
          service_type_id: item.service_type_id || null,
        }));

        const { error: invoiceItemsError } = await supabase.from("invoice_items").insert(invoiceItems);
        
        if (invoiceItemsError) {
          labLogger.error("Failed to create invoice items", invoiceItemsError, { invoiceId: invoice.id });
          throw new Error(`Failed to create invoice items: ${invoiceItemsError.message}`);
        }

        // Update lab order with invoice reference
        const { error: linkError } = await (supabase as any)
          .from("lab_orders")
          .update({ invoice_id: invoice.id })
          .eq("id", order.id);
          
        if (linkError) {
          labLogger.error("Failed to link invoice to lab order", linkError, { orderId: order.id, invoiceId: invoice.id });
        }
      }

      labLogger.info("Lab order created", { 
        orderId: order.id,
        orderNumber: order.order_number,
        testsCount: items.length,
        invoiceId: invoice?.id
      });

      return { order, invoice };
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ["lab-orders"] });
      queryClient.invalidateQueries({ queryKey: ["lab-order-by-consultation"] });
      queryClient.invalidateQueries({ queryKey: ["invoices"] });
    },
  });
}

// Update lab order payment status
export function useUpdateLabOrderPayment() {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: async ({
      orderId,
      paymentStatus,
      invoiceId,
    }: {
      orderId: string;
      paymentStatus: "pending" | "paid" | "partial" | "waived";
      invoiceId?: string;
    }) => {
      const updateData: Record<string, unknown> = { payment_status: paymentStatus };
      if (invoiceId) updateData.invoice_id = invoiceId;

      const { data, error } = await (supabase as any)
        .from("lab_orders")
        .update(updateData)
        .eq("id", orderId)
        .select()
        .single();

      if (error) throw error;
      return data;
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ["lab-orders"] });
      queryClient.invalidateQueries({ queryKey: ["lab-order"] });
    },
  });
}

